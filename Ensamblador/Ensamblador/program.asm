# ========================================
# SECCIÓN 1: Instrucciones Tipo I (Inmediato)
# ========================================
addi x10, x0, 10      # x10 = 0 + 10 = 0x0000000A
addi x11, x0, 20      # x11 = 0 + 20 = 0x00000014

# ========================================
# SECCIÓN 2: Instrucciones Tipo R (Registro-Registro)
# ========================================
add  x12, x10, x11    # x12 = x10 + x11 = 10 + 20 = 0x0000001E (30)
addi x11, x11, -1     # x11 = x11 - 1 = 20 - 1 = 0x00000013 (19)
add  x13, x10, x11    # x13 = x10 + x11 = 10 + 19 = 0x0000001D (29)

# ========================================
# SECCIÓN 3: Más operaciones Tipo R
# ========================================
addi x14, x0, 0       # x14 = 0 (preparar para SUB)
sub  x14, x12, x13    # x14 = x12 - x13 = 30 - 29 = 0x00000001
and  x15, x10, x14    # x15 = x10 & x14 = 0xA & 0x1 = 0x00000000

# ========================================
# SECCIÓN 5: Instrucciones Tipo S (Store)
# ========================================
sw   x10, 0(x0)       # MEM[0 + 0] = x10 = 0x0000000A
sw   x12, 4(x0)       # MEM[0 + 4] = x12 = 0x0000001E

# ========================================
# SECCIÓN 4: Instrucciones Tipo I (Load)
# ========================================
lw   x16, 4(x0)      # x16 = MEM[x14 + 0] = MEM[1]
lh   x17, 0(x14)      # x17 = MEM[x14 + 0] (half-word signed)

